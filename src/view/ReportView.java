    private void btGerarReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGerarReportActionPerformed
        Connection conn = new ConnectionFactory().getConexao();
        String src = "report_gerencia.jasper";
        JasperPrint jasperprint = null;
        try {
        jasperprint = JasperFillManager.fillReport(src, null, conn);
        } catch (Exception erro) {
            throw new RuntimeException("Erro 2: "+erro);
        }
        JasperViewer view = new JasperViewer(jasperprint,false);
        view.setVisible(true);
    }//GEN-LAST:event_btGerarReportActionPerformed

